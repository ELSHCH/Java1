public class News {
    String title;
    String text;
    int numNews;
    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getNumNews() {
        return numNews;
    }

    public void setNumNews(int numNews) {
        this.numNews = numNews;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
